<h3>QOLOGY DIRECT, LLC <br />
	Do Not Call Policy (TCPA 47 USCS &sect; 227) </h3>
<p>Updated December 22, 2014</p>
<p class="noclass">Consumers may request by telephone or in writing, that our company place their name on our company-specific Do Not Call list. Although we may ask for their names, address and telephone number, we will accept a request even if the consumer only wishes to provide their telephone number. Consumers have the right to have calls stopped and to be put on our Do Not Call list, even if they are still a customer. The specific reason or severity of the tone of the request is not important. <strong>ANY REQUESTS WILL BE HONORED</strong>.</p>
<p>When we receive a request, the telephone number is added to our Do Not Call list within 20 calendar days. In some cases it may take up to 30 calendar days for the consumerâ€™s telephone to be removed from other company wide calling lists. A Do Not Call request stays on our list for at least 5 years. If the consumer moves, changes their telephone or adds additional telephone numbers, the consumer must provide us with the new telephone number in order for us to prevent calls to that number. We will not share the information customers provide with anyone except affiliated companies or subsidiaries without their prior written permission.</p>
<p>Other companies may perform telemarketing services on our behalf. In this case, we will also forward our updated Do Not Call list(s) to these companies on a monthly basis.</p>
<p>All employees and Representatives that engage in outbound telephone solicitation are trained in this policy and made aware of these procedures before they are allowed to place calls to consumers. Managers, supervisors, or trainers must review this policy with these employees annually. Re-certification of training occurs on an as needed basis.</p>
<p>If the consumer notifies us that they have received a telephone call from us after they have already requested to be placed on our Do Not Call list, we will apologize for the intrusion and have a manager or supervisor investigate. The manager or supervisor will also personally confirm that the telephone numbers is on the Do Not Call list. Upon request, we will mail the consumer a confirmation that their telephone number is on the list.</p>
<p>The Federal Trade Commission offers a free service to consumers that allow them to place their residential phone number on a National Do Not Call Registry. This service will reduce the number of telemarketing calls received. Consumers may add their residential phone number to the list either by calling 888-382-1222 or through the internet at www.donotcall.gov. It may take several weeks before they notice a reduction in calls.</p>
<p>The methods and procedures in this Do Not Call policy are reviewed by the Do Not Call Compliance Monitoring and Enforcement Committee on a quarterly basis.</p>
<p>If you have questions, comments or concerns, please contact our compliance department at 954-657-9600</p>
